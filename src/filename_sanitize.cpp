// ===========================================================================
// Pass 1: forbidden byte replacement
// ===========================================================================

std::vector<uint8_t> replace_forbidden_bytes(std::span<const uint8_t> input) noexcept {
    // Forbidden: 0x00-0x1F (control characters), 0x2F ('/'), 0x5C ('\').
    auto is_forbidden = [](uint8_t b) noexcept -> bool {
        return b <= 0x1F || b == 0x2F || b == 0x5C;
    };

    std::vector<uint8_t> out;
    out.reserve(input.size());

    size_t i = 0;
    while (i < input.size()) {
        if (is_forbidden(input[i])) {
            // Consume the entire contiguous run of forbidden bytes.
            while (i < input.size() && is_forbidden(input[i])) ++i;
            // Replace the whole run with a single '_'.
            out.push_back(0x5F);
        } else {
            out.push_back(input[i++]);
        }
    }
    return out;
}

// ===========================================================================
// Pass 2: invalid-UTF-8 replacement
// ===========================================================================

/// Returns the expected continuation-byte count for a leading byte,
/// or 0 if the byte is not a valid leading byte.
static int utf8_seq_len(uint8_t b) noexcept {
    if (b < 0x80) return 1;         // ASCII (0x00-0x7F)
    if (b < 0xC2) return 0;         // Continuation or overlong 2-byte
    if (b < 0xE0) return 2;
    if (b < 0xF0) return 3;
    if (b < 0xF5) return 4;
    return 0;                        // > U+10FFFF
}

/// Validate one complete UTF-8 sequence starting at input[i].
/// Returns the sequence length (1-4) if valid, 0 otherwise.
/// Does not check for surrogates (handled by is_valid_scalar check).
static int validate_utf8_seq(std::span<const uint8_t> input, size_t i) noexcept {
    const int len = utf8_seq_len(input[i]);
    if (len == 0) return 0;
    if (len == 1) return 1;  // ASCII always valid

    // Verify we have enough bytes.
    if (i + static_cast<size_t>(len) > input.size()) return 0;

    // For 3-byte and 4-byte sequences, first continuation byte has extra constraints.
    uint8_t b0 = input[i];
    uint8_t b1 = input[i + 1];

    if (len == 2) {
        // 0xC2-0xDF + 0x80-0xBF
        if (b1 < 0x80 || b1 > 0xBF) return 0;
        return 2;
    }

    if (len == 3) {
        // Special first-continuation constraints (overlong / surrogates).
        if (b0 == 0xE0 && (b1 < 0xA0 || b1 > 0xBF)) return 0;  // overlong
        if (b0 == 0xED && (b1 < 0x80 || b1 > 0x9F)) return 0;  // surrogate
        if (b1 < 0x80 || b1 > 0xBF) return 0;
        uint8_t b2 = input[i + 2];
        if (b2 < 0x80 || b2 > 0xBF) return 0;
        return 3;
    }

    // len == 4
    // Special first-continuation constraints (overlong / > U+10FFFF).
    if (b0 == 0xF0 && (b1 < 0x90 || b1 > 0xBF)) return 0;
    if (b0 == 0xF4 && (b1 < 0x80 || b1 > 0x8F)) return 0;
    if (b1 < 0x80 || b1 > 0xBF) return 0;
    uint8_t b2 = input[i + 2];
    uint8_t b3 = input[i + 3];
    if (b2 < 0x80 || b2 > 0xBF) return 0;
    if (b3 < 0x80 || b3 > 0xBF) return 0;
    return 4;
}

std::vector<uint8_t> replace_invalid_utf8(std::span<const uint8_t> input) noexcept {
    std::vector<uint8_t> out;
    out.reserve(input.size());

    size_t i = 0;
    while (i < input.size()) {
        int valid_len = validate_utf8_seq(input, i);
        if (valid_len > 0) {
            // Valid sequence - copy verbatim.
            for (size_t k = 0; k < static_cast<size_t>(valid_len); ++k) out.push_back(input[i + k]);
            i += static_cast<size_t>(valid_len);
        } else {
            // Invalid byte - consume ONE byte (maximal subpart = 1 byte when
            // the initial byte is not a valid sequence start), emit '_'.
            out.push_back(0x5F);
            ++i;
        }
    }
    return out;
}

// ===========================================================================
// Full sanitization
// ===========================================================================

Result<std::string> sanitize_filename(std::span<const uint8_t, 255> raw_field) {
    // Step 1: find effective filename (bytes before first null).
    size_t null_pos = 255;
    for (size_t i = 0; i < 255; ++i) {
        if (raw_field[i] == 0x00) { null_pos = i; break; }
    }

    // Step 2: bytes after null MUST all be zero (Section 4.8).
    for (size_t i = null_pos + 1; i < 255; ++i) {
        if (raw_field[i] != 0x00) {
            return std::unexpected(SfcError{
                ErrorCode::NonZeroAfterFilenameNull,
                std::format("non-zero byte 0x{:02X} at filename offset {}", raw_field[i], i)
            });
        }
    }

    // Effective bytes: [0, null_pos).
    auto effective = raw_field.subspan(0, null_pos);

    // Step 3: Pass 1 - forbidden bytes.
    auto pass1 = replace_forbidden_bytes(effective);

    // Step 4: Pass 2 - invalid UTF-8.
    auto pass2 = replace_invalid_utf8(pass1);

    // Step 5: reject empty (Section 4.8 rule b).
    if (pass2.empty()) {
        return std::unexpected(SfcError{
            ErrorCode::EmptyInnerFilename,
            "sanitized filename is empty"
        });
    }

    std::string result(pass2.begin(), pass2.end());

    // Step 6: reject "." and ".." as the entire filename (Section 4.8).
    if (result == "." || result == "..") {
        return std::unexpected(SfcError{
            ErrorCode::EmptyInnerFilename,
            std::format("sanitized filename '{}' is a reserved name", result)
        });
    }

    return result;
}

