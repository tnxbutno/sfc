/// Decode one UTF-8 codepoint from s[i..].
/// Returns {codepoint, bytes_consumed}; on error returns {0xFFFD, 1}.
static std::pair<uint32_t, size_t>
utf8_decode(const std::string& s, size_t i) noexcept {
    uint8_t b0 = static_cast<uint8_t>(s[i]);
    if (b0 < 0x80) return {b0, 1};

    auto cont = [&](size_t k) noexcept -> uint8_t {
        return (i + k < s.size()) ? static_cast<uint8_t>(s[i + k]) : 0;
    };

    if (b0 < 0xE0 && b0 >= 0xC2) {
        uint32_t cp = (static_cast<uint32_t>(b0 & 0x1Fu) << 6u) |
                      static_cast<uint32_t>(cont(1) & 0x3Fu);
        return {cp, 2};
    }
    if (b0 < 0xF0) {
        uint32_t cp = (static_cast<uint32_t>(b0 & 0x0Fu) << 12u) |
                      (static_cast<uint32_t>(cont(1) & 0x3Fu) << 6u) |
                      static_cast<uint32_t>(cont(2) & 0x3Fu);
        return {cp, 3};
    }
    if (b0 < 0xF5) {
        uint32_t cp = (static_cast<uint32_t>(b0 & 0x07u) << 18u) |
                      (static_cast<uint32_t>(cont(1) & 0x3Fu) << 12u) |
                      (static_cast<uint32_t>(cont(2) & 0x3Fu) << 6u) |
                      static_cast<uint32_t>(cont(3) & 0x3Fu);
        return {cp, 4};
    }
    return {0xFFFD, 1};  // invalid
}

/// Encode a codepoint as UTF-8 into out.
static void utf8_encode(uint32_t cp, std::string& out) noexcept {
    if (cp < 0x80) {
        out += static_cast<char>(cp);
    } else if (cp < 0x800) {
        out += static_cast<char>(0xC0 | (cp >> 6));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else if (cp < 0x10000) {
        out += static_cast<char>(0xE0 | (cp >> 12));
        out += static_cast<char>(0x80 | ((cp >> 6) & 0x3F));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else {
        out += static_cast<char>(0xF0 | (cp >> 18));
        out += static_cast<char>(0x80 | ((cp >> 12) & 0x3F));
        out += static_cast<char>(0x80 | ((cp >> 6) & 0x3F));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    }
}

