std::string case_fold(const std::string& utf8) noexcept {
    std::string result;
    result.reserve(utf8.size());
    size_t i = 0;
    while (i < utf8.size()) {
        auto [cp, len] = utf8_decode(utf8, i);
        i += len;
        utf8_encode(fold_cp(cp), result);
    }
    return result;
}

// ===========================================================================
// Collision detection
// ===========================================================================

bool paths_collide(const std::string& a, const std::string& b) noexcept {
    // Two paths collide if they are identical after Unicode Simple Case Folding.
    return case_fold(a) == case_fold(b);
}

